package com.tianyi.yw.model;

public class UserType {
    private Integer id;

    private Integer userId;

    private String maintainanceKey;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getMaintainanceKey() {
        return maintainanceKey;
    }

    public void setMaintainanceKey(String maintainanceKey) {
        this.maintainanceKey = maintainanceKey;
    }
}